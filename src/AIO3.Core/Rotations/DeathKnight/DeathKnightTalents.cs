    /// Default talent build per Death Knight spec. The old AIO DK FC shipped NO build strings (it assumed a
    /// pre-specced character), so these are authored sane solo-leveling builds (like the other AIO3 classes'
    /// Talents). The trainer applies the progression in order, spending points as they become available while
    /// leveling.
    ///
    /// Each string is one progression snapshot: exactly 88 characters, one per talent, in in-game tree order
    /// (the same order <c>GetTalentInfo(tab, index)</c> iterates — row by row, left→right by column). The trees
    /// are laid out Blood tab first, then Frost, then Unholy, matching the real 3.3.5a DK talent counts:
    ///   Blood 28 + Frost 29 + Unholy 31 = 88.
    /// So chars [0..27] are Blood, [28..56] are Frost, [57..87] are Unholy; each char is that talent's target
    /// rank ('0'..'5'). This MUST equal the live GetNumTalents(1)+(2)+(3) sum the trainer computes, or the code
    /// is rejected. Builds go deep into a single tree (a legal, gating-respecting ~71-point leveling spec) and
    /// front-load the spec's key talents as early as the tree gates allow (the ones the rotation keys on:
    /// Heart Strike / Dancing Rune Weapon for Blood, Killing Machine / Rime / Frost Strike / Howling Blast for
